package zselimdeneme;

public class switch01 {
    public static void main(String[] args) {







            for(int i=1; i<10; i=i+1) {
                if(i%3==0) {
                    System.out.print(i + " ");
                    System.out.println();

                    char ch = 'a';
                    while(ch<'d') {
                        System.out.print(ch + " ");
                        ch++; }

                    int product=1;
                    int num=1;
                    while (num<5){
                       product=product*num;
                       num++;}
                        System.out.println(product);
                    }
















        }
















}}
