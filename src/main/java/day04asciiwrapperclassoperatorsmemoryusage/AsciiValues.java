package day04asciiwrapperclassoperatorsmemoryusage;

public class AsciiValues {
    //Javada her karakterin sayısal bir değeri vardır.
    // bu değerler ASCII değerler olarak adlandrırlır.
    //bu degerlerin tamamının bulunduğu tabloya ASCII table denir.
        public static void main(String[] args) {
        //herhangi bir karakterin ASCII değerini bulmak için int data typeında bir variable a koyunuz.
        int ch='A';
        System.out.println(ch);
        int unlem='!';
        System.out.println(unlem);
        char c1='K';
        char c2='?';
        //Javada charları matematiksel işlemlerde kullanırsanız java o charların ASCII değerlerini kullanır.
        System.out.println(c1+c2);





    }
}
